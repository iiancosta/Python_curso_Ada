# Operadores Aritméticos:
a = 10
b = 3

print("Soma:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Divisão:", a / b)
print("Divisão Inteira:", a // b)
print("Resto da Divisão:", a % b)
print("Exponenciação:", a ** b)

# Operadores Booleanos:
x = True
y = False

print("Resultado 'and':", x and y)
print("Resultado 'or':", x or y)
print("Resultado 'not x':", not x)


# Comparadores:
a = 10
b = 5

print("Igual a:", a == b)
print("Diferente de:", a != b)
print("Maior que:", a > b)
print("Menor que:", a < b)
print("Maior ou igual a:", a >= b)
print("Menor ou igual a:", a <= b)